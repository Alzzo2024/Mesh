export function EmojiPicker({ onPick, align = "left" }: { onPick: (e: string) => void; align?: "left" | "right" }) {
  const [open, setOpen] = useState(false);
  const [tab, setTab] = useState(0);
  const ref = useRef<HTMLDivElement>(null);

  useEffect(() => {
    if (!open) return;
    const handler = (e: MouseEvent) => {
      if (ref.current && !ref.current.contains(e.target as Node)) setOpen(false);
    };
    window.addEventListener("mousedown", handler);
    return () => window.removeEventListener("mousedown", handler);
  }, [open]);

  return (
    <div className="relative" ref={ref}>
      <button
        type="button"
        onClick={() => setOpen((v) => !v)}
        className="p-2 rounded-full hover:bg-secondary text-muted-foreground"
        aria-label="Emojis"
      >
        <Smile className="h-5 w-5" />
      </button>
      {open && (
        <div
          className={`fixed sm:absolute sm:bottom-full sm:mb-2 z-50 bg-popover border border-border shadow-xl overflow-hidden
            sm:rounded-2xl sm:w-72 sm:max-w-[calc(100vw-1rem)]
            inset-x-2 bottom-2 rounded-2xl w-auto max-h-[70vh] sm:max-h-72 flex flex-col
            ${align === "right" ? "sm:right-0 sm:left-auto" : "sm:left-0 sm:right-auto"}`}
        >
          <div className="flex overflow-x-auto border-b border-border shrink-0">
            {CATEGORIES.map((c, i) => (
              <button
                key={i}
                type="button"
                onClick={() => setTab(i)}
                className={`flex-1 min-w-[2.25rem] text-lg py-2 hover:bg-secondary ${i === tab ? "bg-secondary" : ""}`}
              >
                {c.label}
              </button>
            ))}
          </div>
          <div className="flex-1 overflow-y-auto p-2 grid grid-cols-8 gap-1">
            {CATEGORIES[tab].emojis.map((e, i) => (
              <button
                key={`${tab}-${i}`}
                type="button"
                onClick={() => onPick(e)}
                className="text-xl rounded hover:bg-secondary p-1 emoji-font"
              >
                {e}
              </button>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
